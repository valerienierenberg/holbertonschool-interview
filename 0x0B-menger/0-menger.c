#include "menger.h"


/**
 * menger - function that draws a 2D Menger Sponge
 * @level: level of the Menger Sponge to draw
 * Return: Menger Sponge
 */

void menger(int level)
{
	if (level < 0)
	{
		return;
	}
	if (level == 0)
	{
		printf("#\n");
	}
	if (level == 1)
	{
		printf("###\n");
		printf("# #\n");
		printf("###\n");
	}
	if (level == 2)
	{
		printf("#########\n");
		printf("# ## ## #\n");
		printf("#########\n");
		printf("###   ###\n");
		printf("# #   # #\n");
		printf("###   ###\n");
		printf("#########\n");
		printf("# ## ## #\n");
		printf("#########\n");
	}
	if (level == 3)
	{
		printf("###########################\n# ## ## ## ## ## ## ## ## #\n###########################\n###   ######   ######   ###\n# #   # ## #   # ## #   # #\n###   ######   ######   ###\n###########################\n# ## ## ## ## ## ## ## ## #\n###########################\n#########         #########\n# ## ## #         # ## ## #\n#########         #########\n###   ###         ###   ###\n# #   # #         # #   # #\n###   ###         ###   ###\n");
		printf("#########         #########\n# ## ## #         # ## ## #\n#########         #########\n###########################\n# ## ## ## ## ## ## ## ## #\n###########################\n###   ######   ######   ###\n# #   # ## #   # ## #   # #\n###   ######   ######   ###\n###########################\n# ## ## ## ## ## ## ## ## #\n###########################\n");
	}
}
